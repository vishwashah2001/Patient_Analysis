    private void LogInUsernameInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LogInUsernameInputKeyReleased
        // TODO add your handling code here:
        int count = 0;
        String type = (String) LogInTypeInput.getSelectedItem();
        for(Person p:person.getPerson()) {
            String test = p.getPersonType();
            if(test.equals(type)) {
                count++;
            }
        }
        if(count > 0) {
            LogInUsernameErrMsg.setText(null);
        } else if(type.equals(SYSUSERNAME)) {
            LogInUsernameErrMsg.setText(null);
        } else {
            LogInUsernameErrMsg.setText("Please enter valid Username");
        }
    }//GEN-LAST:event_LogInUsernameInputKeyReleased

