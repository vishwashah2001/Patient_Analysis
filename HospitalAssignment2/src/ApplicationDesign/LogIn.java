    private void LogInPasswordInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LogInPasswordInputKeyReleased
        // TODO add your handling code here:
        String validation="^[a-zA-Z0-9]{11}";
        Pattern test = Pattern.compile(validation);
        Matcher ontest = test.matcher(LogInPasswordInput.getText());
        if(!ontest.matches() || LogInPasswordInput.getText().equals(SYSPASS)){
            LogInPassErrMsg.setText("Password lengh must be 11");
        }
        else {
            LogInPassErrMsg.setText(null);
        }
    }//GEN-LAST:event_LogInPasswordInputKeyReleased

