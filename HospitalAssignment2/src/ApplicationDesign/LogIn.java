    private void LogInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInButtonActionPerformed
        // TODO add your handling code here:
         String username = LogInUsernameInput.getText();
        String pass = LogInPasswordInput.getText();
        String type = LogInTypeInput.getSelectedItem().toString();
                
        if(username.equals(SYSUSERNAME) && pass.equals(SYSPASS) && type.equals("System Admin")) {
            this.dispose();
            AdminFrame.Instance.tmpLogInText.setText("Log in into another account?");
            AdminFrame.Instance.tmpLogInButton.setText("Log Out");
            
            SysAdmMainFrame systemAdminAddPerson = new SysAdmMainFrame(person, hospital, community, booking, encounter);
            AdminFrame.Instance.tmpMainSplit.setBottomComponent(systemAdminAddPerson);
            
        } else {
            int count = 0;
            for(Person p:person.getPerson()) {
                String testUsername = p.getPersonEmail();
                String testPass = p.getPass();
                String testType = p.getPersonType();
                if(testUsername.equals(username) && testPass.equals(pass) && testType.equals(type)) {
                    count++;
                }
            }
            if(count == 1) {
                this.dispose();
                AdminFrame.Instance.tmpLogInText.setText("Log in into another account?");
                AdminFrame.Instance.tmpLogInButton.setText("Log Out");
                
                switch (type) {
                    case "Patient" -> {
                        PatientMainFrame patientMainFrame = new PatientMainFrame(person, hospital, community, booking, encounter);
                        AdminFrame.Instance.tmpMainSplit.setBottomComponent(patientMainFrame);
                    }
                    case "Doctor" -> {
                        DoctorMainFrame doctorMainFrame = new DoctorMainFrame(person, hospital, community, booking, encounter);
                        AdminFrame.Instance.tmpMainSplit.setBottomComponent(doctorMainFrame);
                    }
                    case "Community Admin" -> {
                        ComAdminMainFrame comAdminMainFrame = new ComAdminMainFrame(person, hospital, community, booking, encounter);
                        AdminFrame.Instance.tmpMainSplit.setBottomComponent(comAdminMainFrame);
                    }
                    case "Hospital Admin" -> {
                        HospitalAdminsMainFrame hospitalAdminsMainFrame = new HospitalAdminsMainFrame(person, hospital, community, booking, encounter);
                        AdminFrame.Instance.tmpMainSplit.setBottomComponent(hospitalAdminsMainFrame);
                    }
                    default -> {
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this,"Entered details does not match with records");
            }
        }
    }//GEN-LAST:event_LogInButtonActionPerformed

